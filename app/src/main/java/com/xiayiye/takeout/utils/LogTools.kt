object LogTools {
    val isShowLog = true
    fun showPrintln(anyData: Any) {
        if (isShowLog) {
            println(anyData)
        }
    }

    fun showLog(left: String, right: Any) {
        if (isShowLog) {
            Log.e(left, "${right}")
        }
    }
}